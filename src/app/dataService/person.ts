export class Person {
    
  public  Name:string;
    public Mobile:number;
   public Location:string;
   public  Flat:string;
    public Landmark:string;
   public type:string;

    constructor(name:string,mob:number,location:string,flat:string,landmark:string,type:string) {
        this.Name =name;
        this.Mobile = mob;
        this.Location = location;
        this.Flat= flat;
        this.Landmark = landmark;
        this.type = type;
    }
}
